public class CalculosUnidade{

    public static double quilometroMilha(double km){
        return km*0.62137;
    }
    public static double metrosAcres(double metrosPacre){
        return metrosPacre*0.000247105;

    }
    public static double centimetroParaMetro(double metrosPcentimetro){
        return metrosPcentimetro*10000;
    }
    public static double metrosPes( double metros){
        return metros*3.2808;
    }
    public static double metroPe(double metroQuadrado){
        return metroQuadrado*10.76;
    }

    public static double peCentimetros(double peQuadrado){
        return peQuadrado*0.929;
    }

    public static double milhaAcre(double milhaQuadrado){
        return milhaQuadrado*640;
    }

    public static double acrePes(double acreQuadrado){
        return acreQuadrado*43.560;
    }

 
}